import streamlit as st

def render_manual():
    st.markdown("### 📖 战术指挥官：核心作战指标与术语白皮书")
    st.info("💡 **指挥官寄语**：本手册旨在建立全军统一的战术语言。它不仅解释了底层的数学与物理逻辑，更直接点明了它们在电力现货交易、长协签订与 VPP 调度中的【实战套利与风控价值】。")
    
    st.divider()

    # ==========================================
    # 模块 1：物理形态与能量基因
    # ==========================================
    st.markdown("#### 🌊 一、 物理形态与能量基因 (Physical & Energy Traits)")
    
    with st.expander("📌 1. 负荷率 (Load Factor)", expanded=True):
        st.markdown("""
        * **数学定义**：`日均负荷 / 日最大负荷`。值介于 0 到 1 之间。
        * **物理含义**：反映用户用电的平稳程度。值越高，曲线越像一条水平直线；值越低，说明全天的电量都集中在某几个尖刺时段。
        * **⚔️ 交易应用**：
            * **优质资产**：高负荷率用户（如 85% 以上的化工/数据中心）是**绝对的优质底仓**。可以直接在批发市场以便宜的均价购买“全天带载长协”来覆盖。
            * **劣质资产**：低负荷率用户会在高峰期（现货最贵的时候）疯狂用电。对其报价必须附加高额的“容量风险溢价”。
        """)

    with st.expander("📌 2. 基荷占比 (Base Load Ratio) 与 柔性深度 (Flexibility Depth)", expanded=False):
        st.markdown("""
        * **数学定义**：通过 **FFT（快速傅里叶变换）** 将负荷时域曲线转化为频域。
            * **基荷占比** = 零频振幅（绝对直流分量）/ 总能量振幅。
            * **柔性深度** = 高频振幅（波动分量）/ 总均值。
        * **物理含义**：基荷代表工厂里“永远不能关停的底座设备”；高频代表“频繁启停、可被随时掐断或调节的扰动设备”（如温控空调、流水线电机）。
        * **⚔️ 交易应用**：
            * **基荷极高**：适合签署绝对固定的长协合同（死水电量）。
            * **柔性极深**：这是 **虚拟电厂（VPP）和需求响应（DR）的最爱**！在现货电价飙升至 1.5元/度 的极端时刻，您可以直接通知这些用户“停机 2 小时并给予补贴”，从而避免公司在现货市场高价买电爆仓。
        """)

    # ==========================================
    # 模块 2：AI 侦测与异动标签
    # ==========================================
    st.markdown("#### 🕵️‍♂️ 二、 AI 侦测与异动标签 (AI Detection Tags)")
    
    with st.expander("📌 3. 正午塌陷指数 (Noon Collapse Index) -> [疑似光伏]", expanded=False):
        st.markdown("""
        * **数学定义**：`午间 (11:00-14:00) 平均负荷 / 全天均值`。
        * **物理含义**：中午不仅没有高峰，反而出现巨大的负荷深坑（即“鸭子曲线”现象）。
        * **⚔️ 交易应用**：揪出**表后隐性光伏（BTM PV）**。这类用户极其危险！一旦某天突然阴天/暴雨，光伏不发电，他们会瞬间向电网抽取巨量负荷，导致售电公司在实时现货市场面临灾难性的“偏差考核罚款”。必须对他们引入“气象风险定价”。
        """)

    with st.expander("📌 4. 夜间阶跃与极高负荷率 -> [疑似储能]", expanded=False):
        st.markdown("""
        * **物理含义**：在深夜谷电时段（如 0:00-08:00）负荷呈现一条极其平整的“高台”，且启停瞬间曲线呈直角（阶跃）。
        * **⚔️ 交易应用**：这种用户本身就是在做“峰谷套利”。他们是网内的“海绵”，您可以尝试与其签署分成协议，将其储能资产直接接入公司的统一调度平台，用于平抑整个资产池的偏差。
        """)

    # ==========================================
    # 模块 3：系统级组合与风控
    # ==========================================
    st.markdown("#### ⚙️ 三、 系统级组合与风控 (Portfolio & Risk Management)")

    with st.expander("📌 5. 群体同时率 (Coincidence Factor)", expanded=False):
        st.markdown("""
        * **数学定义**：`资产池整体最高负荷 / (内部每个用户各自的最高负荷之和)`。
        * **物理含义**：衡量池内用户是否在“同一时刻”达到用电巅峰。
        * **⚔️ 交易应用**：**保险公司的核心赚钱逻辑！** 如果同时率为 0.7，意味着您可以卖出 100MW 的需量合同，但实际上只需要向电网购买 70MW 的容量，这 30% 就是纯利润（自然错峰释放的红利）。
        """)

    with st.expander("📌 6. 互补对冲矩阵 (Pearson Correlation Matrix)", expanded=True):
        st.markdown("""
        * **数学定义**：阵营负荷曲线之间的皮尔逊相关系数（值介于 -1 到 1 之间）。
        * **物理含义**：
            * 🔴 **强正相关 (> 0.5)**：大家同频共振，你用电我也用电。
            * 🔵 **强负相关 (< 0)**：天然伴侣，你休息时我刚好在生产。
        * **⚔️ 交易应用**：**寻找黄金搭档！** 严禁只招揽“白天高负荷”的用户（风险极度叠加）。必须把负相关（标蓝）的群体强行打包在一个资产池里。他们之间的波动会互相抵消，最终把整个池子的总负荷拉成一条直线，从而在无风险的情况下赚取批发与零售的差价！
        """)

    with st.expander("📌 7. 洛伦兹曲线与基尼系数 (Lorenz Curve & Gini Coefficient)", expanded=False):
        st.markdown("""
        * **数学定义**：原本用于衡量国家贫富差距。此处 X 轴为“累计用户百分比”，Y 轴为“累计电量百分比”。
        * **物理含义**：衡量大盘电量的“集中度”。曲线越往下凹（基尼系数越接近 1），说明电量越集中在极少数人手里。
        * **⚔️ 交易应用**：**防御黑天鹅风险！** 如果 5% 的用户贡献了池子里 80% 的电量，一旦这几个头部大厂因为订单减少突然停工，售电公司的基本盘将瞬间崩溃，面临巨额的“长协电量不足违约金”。系统需对此类高集中度发出红色预警。
        """)